    private void TableClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableClienteMouseClicked
        // TODO add your handling code here:
        int fila = TableCliente.rowAtPoint(evt.getPoint());
        TxtID.setText(TableCliente.getValueAt(fila, 0).toString());
        TxtNombre.setText(TableCliente.getValueAt(fila, 1).toString());
        TxtDni.setText(TableCliente.getValueAt(fila, 2).toString());
        TxtTelefono.setText(TableCliente.getValueAt(fila, 3).toString());
        TxtDireccion.setText(TableCliente.getValueAt(fila, 4).toString());
        TxtCorreo.setText(TableCliente.getValueAt(fila, 5).toString());

    }//GEN-LAST:event_TableClienteMouseClicked

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        // TODO add your handling code here:
        if (!"".equals(TxtID.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar");
            if (pregunta == 0) {
                int id = Integer.parseInt(TxtID.getText());
                cliente.EliminarCliente(id);
                LimpiarTable();
                LimpiarCliente();
                ListarCliente();
            }
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
        // TODO add your handling code here:
        if ("".equals(TxtID.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        } else {

            if (!"".equals(TxtNombre.getText()) || !"".equals(TxtDni.getText()) || !"".equals(TxtTelefono.getText())
                    || !"".equals(TxtDireccion.getText()) || !"".equals(TxtCorreo.getText())) {
                cl.setNombre(TxtNombre.getText());
                cl.setDni(Integer.parseInt(TxtDni.getText()));
                cl.setTelefono(TxtTelefono.getText());
                cl.setDireccion(TxtDireccion.getText());
                cl.setCorreo(TxtCorreo.getText());
                cl.setId(Integer.parseInt(TxtID.getText()));
                cliente.EditarCliente(cl);
                LimpiarTable();
                LimpiarCliente();
                ListarCliente();
            } else{
                JOptionPane.showMessageDialog(null, "Alguno de los campos esta vacio");
            }
        }
    }//GEN-LAST:event_BtnEditarActionPerformed

