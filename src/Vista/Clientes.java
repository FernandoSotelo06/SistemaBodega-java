    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        // TODO add your handling code here:
        if (!"".equals(TxtNombre.getText()) || !"".equals(TxtDni.getText()) || !"".equals(TxtTelefono.getText()) || !"".equals(TxtDireccion.getText())
                || !"".equals(TxtCorreo.getText())) {
            cl.setNombre(TxtNombre.getText());
            cl.setDni(Integer.parseInt(TxtDni.getText()));
            cl.setTelefono(TxtTelefono.getText());
            cl.setDireccion(TxtDireccion.getText());
            cl.setCorreo(TxtCorreo.getText());
            cliente.RegistrarCliente(cl);
            LimpiarTable();
            LimpiarCliente();
            ListarCliente();
            JOptionPane.showMessageDialog(null, "Cliente Registrado");
        } else {
            JOptionPane.showMessageDialog(null, "Alguno de los campos esta vacio");
        }
    }//GEN-LAST:event_BtnAgregarActionPerformed

