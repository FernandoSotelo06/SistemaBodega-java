    private void TxtCodigoVentasKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtCodigoVentasKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(TxtCodigoVentas.getText())) {
                String cod = TxtCodigoVentas.getText();
                pro = proDao.BuscarPro(cod);
                if (pro.getDescripcion() != null) {
                    TxtDecripcionVentas.setText("" + pro.getDescripcion());
                    TxtPrecioVentas.setText("" + pro.getPrecio());
                    TxtStockVentas.setText("" + pro.getCantidad());
                    TxtCantidadVentas.requestFocus();
                } else {
                    LimpiarVenta();
                    TxtCodigoVentas.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el codigo del productos");
                TxtCodigoVentas.requestFocus();
            }
        }
    }//GEN-LAST:event_TxtCodigoVentasKeyPressed

    private void TxtCantidadVentasKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtCantidadVentasKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(TxtCantidadVentas.getText())) {
                int id = Integer.parseInt(TxtCodigoVentas.getText());
                String descripcion = TxtDecripcionVentas.getText();
                int cant = Integer.parseInt(TxtCantidadVentas.getText());
                double precio = Double.parseDouble(TxtPrecioVentas.getText());
                double total = cant * precio;
                int stock = Integer.parseInt(TxtStockVentas.getText());
                if (stock >= cant) {
                    item = item + 1;
                    DefaultTableModel tmp = (DefaultTableModel) TableVentas.getModel();
                    for (int i = 0; i < TableVentas.getRowCount(); i++) {
                        if (TableVentas.getValueAt(i, 1).equals(TxtDecripcionVentas.getText())) {
                            JOptionPane.showMessageDialog(null, "El producto ya esta registrado");
                            return;
                        }
                    }
                    ArrayList lista = new ArrayList();
                    lista.add(item);
                    lista.add(id);
                    lista.add(descripcion);
                    lista.add(cant);
                    lista.add(precio);
                    lista.add(total);
                    Object[] O = new Object[5];
                    O[0] = lista.get(1);
                    O[1] = lista.get(2);
                    O[2] = lista.get(3);
                    O[3] = lista.get(4);
                    O[4] = lista.get(5);
                    tmp.addRow(O);
                    TableVentas.setModel(tmp);
                    TotalPagar();
                    LimpiarVenta();
                    TxtCodigoVentas.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Stock no disponible");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese Cantidad");
            }
        }
    }//GEN-LAST:event_TxtCantidadVentasKeyPressed

