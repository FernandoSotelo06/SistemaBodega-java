    public void ListarProductos() {
        List<Productos> ListarPro = proDao.ListarProductos();
        modelo = (DefaultTableModel) TableProductos.getModel();
        Object[] ob = new Object[7];
        for (int i = 0; i < ListarPro.size(); i++) {
            ob[0] = ListarPro.get(i).getCodigo();
            ob[1] = ListarPro.get(i).getDescripcion();
            ob[2] = ListarPro.get(i).getCantidad();
            ob[3] = ListarPro.get(i).getPrecio();
            ob[4] = ListarPro.get(i).getCategoria();
            ob[5] = ListarPro.get(i).getVencimiento();
            ob[6] = ListarPro.get(i).getProveedor();
            modelo.addRow(ob);
        }
        TableProductos.setModel(modelo);
    }

    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

