    private void TotalPagar() {
        Totalpagar = 0.00;
        int numFila = TableVentas.getRowCount();
        for (int i = 0; i < numFila; i++) {
            double cal = Double.parseDouble(String.valueOf(TableVentas.getModel().getValueAt(i, 4)));
            Totalpagar = Totalpagar + cal;
        }
        TxtPrecioNeto.setText(String.format("%.2f", Totalpagar));
    }

    private void LimpiarVenta() {
        TxtCodigoVentas.setText("");
        TxtDecripcionVentas.setText("");
        TxtCantidadVentas.setText("");
        TxtCantidadVentas.setText("");
        TxtStockVentas.setText("");
        TxtPrecioVentas.setText("");
    }

    private void RegistrarVenta() {
        String clientev = TxtNombreVentas.getText();
        String vendedor = LabelVendedor.getText();
        double monto = Totalpagar;
        v.setCliente(clientev);
        v.setVendedor(vendedor);
        v.setTotal(monto);
        Vdao.RegistrarVenta(v);
    }

    private void ActualizarStock() {
        for (int i = 0; i < TableVentas.getRowCount(); i++) {
            String cod = TableVentas.getValueAt(i, 0).toString();
            int cant = Integer.parseInt(TableVentas.getValueAt(i, 2).toString());
            pro = proDao.BuscarPro(cod);
            int StockActual = pro.getCantidad() - cant;
            Vdao.ActualizarStock(StockActual, cod);

        }
    }

    private void RegistrarDetalle() {
        int id = Vdao.IdVenta();
        for (int i = 0; i < TableVentas.getRowCount(); i++) {
            String cod = TableVentas.getValueAt(i, 0).toString();
            int cant = Integer.parseInt(TableVentas.getValueAt(i, 2).toString());
            double precio = Double.parseDouble(TableVentas.getValueAt(i, 3).toString());
            Dv.setCod_pro(cod);
            Dv.setCantidad(cant);
            Dv.setPrecio(precio);
            Dv.setId(id);
            Vdao.RegistrarDetalle(Dv);

        }
    }

    private void LimpiarTableVenta() {
        tmp = (DefaultTableModel) TableVentas.getModel();
        int fila = TableVentas.getRowCount();
        for (int i = 0; i < fila; i++) {
            tmp.removeRow(0);
        }
    }

